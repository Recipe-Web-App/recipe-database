-- db/init/users/001_create_DB_MAINT_USER-template.sql

DO
$$
BEGIN
  IF NOT EXISTS (
    SELECT FROM pg_catalog.pg_roles WHERE rolname = '$DB_MAINT_USER'
  ) THEN
    EXECUTE format(
      'CREATE USER %I WITH PASSWORD %L',
      '$DB_MAINT_USER',
      '$DB_MAINT_PASSWORD'
    );
  END IF;
END
$$;

DO
$$
BEGIN
  EXECUTE format('GRANT ALL PRIVILEGES ON SCHEMA recipe_manager TO %I;', '$DB_MAINT_USER');
  EXECUTE format('GRANT ALL PRIVILEGES ON ALL TABLES IN SCHEMA recipe_manager TO %I;', '$DB_MAINT_USER');
  EXECUTE format('GRANT ALL PRIVILEGES ON ALL SEQUENCES IN SCHEMA recipe_manager TO %I;', '$DB_MAINT_USER');
  EXECUTE format('GRANT ALL PRIVILEGES ON ALL FUNCTIONS IN SCHEMA recipe_manager TO %I;', '$DB_MAINT_USER');
  EXECUTE format('ALTER DEFAULT PRIVILEGES IN SCHEMA recipe_manager GRANT ALL PRIVILEGES ON TABLES TO %I;', '$DB_MAINT_USER');
  EXECUTE format('ALTER DEFAULT PRIVILEGES IN SCHEMA recipe_manager GRANT ALL PRIVILEGES ON SEQUENCES TO %I;', '$DB_MAINT_USER');
  EXECUTE format('ALTER DEFAULT PRIVILEGES IN SCHEMA recipe_manager GRANT ALL PRIVILEGES ON FUNCTIONS TO %I;', '$DB_MAINT_USER');
END
$$;
